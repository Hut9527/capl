package com.sunjays.capl.entity;

public class UserAttendance {
    private Integer id;

    private Integer uid;

    private String curvetime;

    private String attendance;

    private String worktime;

    private String remark;
    
    private String name;
    
    private String company;
    
    private String jobnumber;
    
    

    @Override
	public String toString() {
		return "UserAttendance [id=" + id + ", uid=" + uid + ", curvetime=" + curvetime + ", attendance=" + attendance
				+ ", worktime=" + worktime + ", remark=" + remark + ", name=" + name + ", company=" + company
				+ ", jobnumber=" + jobnumber + "]";
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getJobnumber() {
		return jobnumber;
	}

	public void setJobnumber(String jobnumber) {
		this.jobnumber = jobnumber;
	}

	public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }

    public String getCurvetime() {
        return curvetime;
    }

    public void setCurvetime(String curvetime) {
        this.curvetime = curvetime;
    }

    public String getAttendance() {
        return attendance;
    }

    public void setAttendance(String attendance) {
        this.attendance = attendance;
    }

    public String getWorktime() {
        return worktime;
    }

    public void setWorktime(String worktime) {
        this.worktime = worktime;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
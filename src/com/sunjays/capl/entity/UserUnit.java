package com.sunjays.capl.entity;

public class UserUnit {
    private Integer uuid;

    private String uuname;

    private Integer unitypeid;

    private String uumemo;

    private String uure;
    
    private UnitType unitType;
    
    

    public UnitType getUnitType() {
		return unitType;
	}

	public void setUnitType(UnitType unitType) {
		this.unitType = unitType;
	}

	public Integer getUuid() {
        return uuid;
    }

    public void setUuid(Integer uuid) {
        this.uuid = uuid;
    }

    public String getUuname() {
        return uuname;
    }

    public void setUuname(String uuname) {
        this.uuname = uuname;
    }

    public Integer getUnitypeid() {
        return unitypeid;
    }

    public void setUnitypeid(Integer unitypeid) {
        this.unitypeid = unitypeid;
    }

    public String getUumemo() {
        return uumemo;
    }

    public void setUumemo(String uumemo) {
        this.uumemo = uumemo;
    }

    public String getUure() {
        return uure;
    }

    public void setUure(String uure) {
        this.uure = uure;
    }
}